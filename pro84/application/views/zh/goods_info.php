            <p>【博宝艺术网讯】博宝艺术网评论员甲骨撰文 瓷器一直是拍卖场上与国画、油画并行的"三驾马车"之一，尤其是古代瓷器。但是，随着古董市场价位整体走高，赝品层出不群，我国的国画收藏已经逐渐由收藏古代名家作品转向投资现当代名家画作。博宝艺术网有关专家表示，今年来，随着古瓷特别是官窑瓷器在市场上流通数量日益减少，古代官窑瓷价格不断攀升几乎到顶峰，对于一般的藏家来说，很少有捡漏的珍稀品。一些嗅到市场先机的部分藏家和投资者开始把目光转向当代瓷器，尤其是当代名家作品，市场前景广阔，升值潜力巨大。</p>

<p>中国陶瓷艺术大师刘平，其作品在国内、外有较高的声誉。其名列入多种"辞典"。1997年江西省政府授予"江西工艺美术大师"荣誉称号。博宝宝珍商城中，刘平的两件粉彩作品《梅开五福》瓶和《紫藤麻雀》瓶都是当代名家瓷器中的精美之作，受到网友极大的关注。</p>

<p>陆如釉上彩《幽馨》壶图片由博宝宝珍商城提供</p>

<p>另一位大师陆如先生在博宝宝珍商城中陈列的24件瓷器作品都是瓷器精品，无论从工艺还是将来的升值潜力，都是被博宝艺术网的网友看好的。陆如，别名崎，字淘人，号珠山老民，室名淘乐斋，以釉上釉下花鸟犹以梅兰竹菊享誉艺坛，兼长中国画。1994年被授予"陶瓷世家"称号。2003年被评为首届中国陶瓷艺术大师。他将高雅的格调和深厚的艺术修养融入其陶瓷美术创作中，使作品有着很高的审美品位和审美情趣。其作品立意高、意境美，构图奇、装饰新，笔墨精、色彩雅，给人以笔底春风挥不尽，东涂西抹总开花的意匠神韵。（博宝艺术网评论员甲骨撰文）</p>

<p>【博宝点评】目前市场价位显示出现当代瓷器仍处于"洼地"，它有着相当可观的可选择空间和升值空间。同样，作为新兴的收藏门类，它的潜力巨大，发展前景广阔。而且，就艺术性的角度而言，现当代陶瓷作品中呈现出来的丰富性和多元化，在某些方面与高古瓷器相比是有过之而无不及。</p>
